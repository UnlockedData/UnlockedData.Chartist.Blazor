namespace Chartist.Blazor {
    /// <summary>
    /// Defines a chart's padding
    /// </summary>
    public class ChartPadding
    {
        /// <summary>
        /// Gets or sets the top padding.
        /// </summary>
        /// <value>
        /// The top padding.
        /// </value>
        /// <remarks>
        /// Default is 15
        /// </remarks>
        public double Top { get; set; } = 15;

        /// <summary>
        /// Gets or sets the right padding.
        /// </summary>
        /// <value>
        /// The right padding.
        /// </value>
        /// <remarks>
        /// Default is 5
        /// </remarks>
        public double Right { get; set; } = 15;

        /// <summary>
        /// Gets or sets the bottom padding.
        /// </summary>
        /// <value>
        /// The bottom padding.
        /// </value>
        /// <remarks>
        /// Default is 15
        /// </remarks>
        public double Bottom { get; set; } = 5;

        /// <summary>
        /// Gets or sets the left padding.
        /// </summary>
        /// <value>
        /// The left padding.
        /// </value>
        /// <remarks>
        /// Default is 10
        /// </remarks>
        public double Left { get; set; } = 10;

    }
}
